# Python pavyzdys su realiaisiais skaičiais
a = float(input("Įveskite pirmą realųjį skaičių: "))
b = float(input("Įveskite antrą realųjį skaičių: "))

suma = a + b
skirtumas = a - b
sandauga = a * b
dalmuo = a / b

print("Suma:", suma)
print("Skirtumas:", skirtumas)
print("Sandauga:", sandauga)
print("Dalmuo:", dalmuo)
