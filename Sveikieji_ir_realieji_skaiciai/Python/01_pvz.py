# Sveikųjų skaičių pavyzdys
a = int(input("Įveskite pirmą sveikąjį skaičių: "))
b = int(input("Įveskite antrą sveikąjį skaičių: "))

suma = a + b
print("Suma:", suma)

# Realieji skaičiai (float)
